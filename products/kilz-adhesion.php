	<div class="tab_content" id="tab_1">
		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<ul>
			<li>
				<p>All surfaces must be clean, dry and free of oil, grease, mildew, wax, dust, flaky rust, mill scale, loose paint, chalk and other foreign matter that could interfere with adhesion.</p>
			</li>
			<li>
				<p>If washing is necessary, use a non-soapy detergent or a TSP substitute, rinse well and allow to dry.</p>
			</li>
			<li>
				<p>Remove loose rust, peeling paint and mill scale with a scraper, wire brush or sandpaper.</p>
			</li>
			<li>
				<p>Clean bare metal in accordance with SPC (The Society for Protective Coatings) specifications for the proper surface preparation of metal surfaces.</p>
			</li>
			<li>
				<p>Peeling or Checked Paint: Scrape off loose paint and sand to a smooth surface. Sanding or removal of paint containing lead is hazardous.</p>
			</li>
			<li>
				<p>Mold or Mildew Covered Surfaces: Wash the area with a mildew remover, rinse with water and allow to dry before priming.</p>
			</li>
			<li>
				<p>While KILZ Adhesion Primer is formulated to bond without sanding, it is recommended that a small area be tested for adhesion prior to beginning the job.</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>Eye protection is recommended.</p>
			</li>
			<li>
				<p>Only apply if surface, air and product temperatures are between 50&deg;-90&deg;F (10&deg;-32&deg;C).</p>
			</li>
			<li>
				<p>Apply using a brush, roller or sprayer.</p>
			</li>
			<li>
				<p>Brush – High quality Nylon/Polyester</p>
			</li>
			<li>
				<p>Roller – High quality 3/8-1/2" nap on smooth surfaces or 1/2-3/4" nap on semi-rough or porous surfaces.</p>
			</li>
			<li>
				<p>Airless spray – .015 - .021 tip / 60 mesh filter @ 2,500 – 3,200 PSI</p>
			</li>
			<li>
				<p>For spray applications, a small amount of water (no more than ½ pint per gallon) may be added.</p>
			</li>
			<li>
				<p>Stir thoroughly before and occasionally during use.</p>
			</li>
			<li>
				<p>Prime the entire surface to ensure a uniform appearance of the topcoat.</p>
			</li>
			<li>
				<p>Finishing: Topcoat within 30 days to prevent contamination of the primer before painting.</p>
			</li>
			<li>
				<p>Tinting: May be tinted with up to 2 oz. (60 ml) of universal colorant per gallon. Tinting to a lighter shade than the topcoat is recommended.</p>
			</li>
			<li>
				<p>Coverage: 300 - 400 sq. ft.(28 - 37 m2) per gallon depending on application method and surface porosity.</p>
			</li>
		</ul>
		<h2>Dry Time at 77&deg;F (25&deg;C), 50% RH</h2>
		<ul>
			<li>
				<p>Dries to the touch in 30 minutes.</p>
			</li>
			<li>
				<p>Recoat after 1 hour</p>
			</li>
			<li>
				<p>Lower temperatures, higher humidity, poor ventilation or heavy application can significantly increase dry and recoat times.</p>
			</li>
		</ul>
		<h2>Clean up & Disposal</h2>
		<ul>
			<li>
				<p>Clean equipment and paint spatters with warm, soapy water.</p>
			</li>
			<li>
				<p>If spilled, contain material and remove with an inert absorbent. Dispose of contaminated absorbent, container and unused product in accordance with all current federal, state and local regulations.</p>
			</li>
			<li>
				<p>Do not dispose of this product down a drain. Please consider donating any unused product.</p>
			</li>
			<li>
				<p>For recycling or disposal information contact your local household refuse collection service.</p>
			</li>
		</ul>
