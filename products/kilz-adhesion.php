		<p>KILZ&reg; ADHESION Interior & Exterior Bonding Primer is a water-base, modified-acrylic primer specifically formulated to bond to a variety of ‘tough-to-paint’ surfaces. Its advanced chemistry provides a sound anchor for topcoats while reducing or potentially eliminating the need for sanding dense, glossy surfaces. This product may be used under epoxies, lacquers and products containing Xylene or other hot solvents, provided it’s allowed to dry for 24 hours before topcoating and tested for compatibility in an inconspicuous area before full coat application. Use KILZ Adhesion where ensuring a secure bond is critical in situations when performance of a traditional water or oil-base primer may be questionable. For interior and exterior use on a variety of surfaces including; Kynar&reg;, PVC, vinyl, Formica®, glass, tile, glazed brick, chalky paints, glossy finishes, fiberglass and metals. Not recommended for floors or horizontal surfaces subject to prolonged water contact.


