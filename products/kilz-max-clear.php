		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<ul>
			<li>
				<p>The surface must be clean, free of dust, grease, wax, peeling paint, mold, mildew and wallpaper paste.</p>
			</li>
			<li>
				<p>If washing is necessary, use a non-soapy detergent or TSP substitute. Rinse well and allow to dry completely.</p>
			</li>
			<li>
				<p>Mold or Mildew Covered Surfaces: KILZ MAX CLEAR should not be applied to a surface containing mold or mildew; it should not be used to cover up mold or mildew. Wash the area with a mildew remover, rinse with water and allow to completely dry before coating.</p>
			</li>
			<li>
				<p>Apply two (2) coats of KILZ MAX CLEAR Interior Sealer at 4.0 mils WFT (Wet Film Thickness) per coat or at 1.5 mils DFT (Dry Film Thickness) per coat.</p>
			</li>
			<li>
				<p>Masonry, Brick, Stucco and Plaster: KILZ MAX CLEAR may be used on clean, dry, aged masonry surfaces. New masonry must be completely cured (allowed to cure (dry) at least 30 days) before applying KILZ MAX CLEAR.</p>
			</li>
			<li>
				<p>Fire Restoration: It is critical to clean smoke damaged surfaces thoroughly before priming.</p>
			</li>
			<li>
				<p>Peeling or Checked Paint: Scrape off loose paint and sand to a smooth surface.</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>Eye protection is recommended.</p>
			</li>
			<li>
				<p>Only apply if surface, air and product temperatures are between 50&deg; – 90&deg;F (10&deg; – 32&deg;C).</p>
			</li>
			<li>
				<p>Stir thoroughly before and occasionally during use.</p>
			</li>
			<li>
				<p>Apply using a brush, roller or sprayer</p>
			</li>
			<li>
				<p>Roller – High quality 3/8-1/2” nap on smooth surfaces or 1/2-3/4” nap on semi-rough or porous surfaces.</p>
			</li>
			<li>
				<p>Airless spray - .017 - .021 tip / 60 mesh filter. Up to 5% water may be added for spray application.</p>
			</li>
			<li>
				<p>Prime the entire surface to ensure a uniform appearance of the topcoat.</p>
			</li>
			<li>
				<p>Prime the entire surface to help ensure a uniform appearance of the topcoat.</p>
			</li>
			<li>
				<p>Stainblocking: Do not thin for stainblocking applications. After priming, test for stain bleed-through by applying the topcoat to a small -section. If the stain bleeds through the topcoat, apply a second coat of primer and test again before topcoating.</p>
			</li>
			<li>
				<p>Sealing odors: For persistent odors, apply a second coat of primer before topcoating. If odor continues, a longer dry time is needed before topcoating.</p>
			</li>
		</ul>
		<h2>Dry Time at 77&deg;F (25&deg;C), 50% RH</h2>
		<ul>
			<li>
				<p>To Touch: 30 Minutes</p>
			</li>
			<li>
				<p>To Recoat / Topcoat: 1 Hour</p>
			</li>
			<li>
				<p>Longer dry time required in cooler temperatures and in higher humidity.</p>
			</li>
		</ul>
		<h2>Clean up & Disposal</h2>
		<ul>
			<li>
				<p>Clean equipment and paint spatters with warm, soapy water.</p>
			</li>
			<li>
				<p>If spilled, contain spilled material and remove with an inert absorbent. Dispose of contaminated absorbent, container and unused product in accordance with all current federal, state and local regulations.</p>
			</li>
			<li>
				<p>Do not dispose of this product down a drain.</p>
			</li>
			<li>
				<p>Please consider donating any unused product.</p>
			</li>
			<li>
				<p>For recycling or disposal information, contact your local household refuse collection service.</p>
			</li>
		</ul>
