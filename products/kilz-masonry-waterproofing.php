        <h2>Surface Preparation<sup>&dagger;</sup></h2>
        <ul>
            <li>
                <p>New concrete must be completely cured (30 days).</p>
            </li>
            <li>
                <p>Remove efflorescence, dirt, oil and grease. Clean with a concrete cleaner and degreaser.</p>
            </li>
            <li>
                <p>Remove any mildew stains with a bleach and water solution or similar commercially available mildew remover, then rinse.</p>
            </li>
            <li>
                <p>Etch the masonry surface with a concrete etcher when efflorescence is present.</p>
            </li>
            <li>
                <p>Patch all cracks and holes with a fast-setting hydraulic cement.</p>
            </li>
        </ul>
        <h2>Application</h2>
        <ul>
            <li>
               <p>Stir paint prior to use.</p>
            </li>
            <li>
                <p>Use only when temperatures are between 50&deg;F (10&deg;C) and 90&deg;F (32&deg;C).</p>
            </li>
            <li>
                <p>Apply with a high-quality nylon/polyester or other stiff synthetic filament brush or ¾” nap roller.</p>
            </li>
            <li>
                <p>Two coats are required.</p>
            </li>
            <li>
                <p>Coverage varies by surface. Coverage will be approximately 75 square feet per gallon on rough surfaces; up to 125 square feet per gallon on smooth surfaces.</p>
            </li>
            <li>
                <p>IMPORTANT: If leaking is still present after two coats, it indicates that voids or pinholes are still open. Reapply product to these areas.</p>
            </li>
        </ul>
        <h2>Dry Time at 77&deg;F (25&deg;C), 50% RH:</h2>
        <ul>
            <li>
                <p>Dries to touch in one hour. Longer dry times are required in cooler temperatures or high humidity areas.</p>
            </li>
            <li>
                <p>Recoat in four hours.</p>
            </li>
            <li>
                <p>Full cure should be reached in 7-14 days.</p>
            </li>
        </ul>
        <h2>Clean-Up & Disposal</h2>
        <ul>
            <li>
                <p>Clean equipment and paint spatters with warm, soapy water.</p>
            </li>
            <li>
                <p>If spilled, contain spilled material and remove with an inert absorbent. Dispose of contaminated absorbent, container and unused product in accordance with all current federal, state and local regulations.</p>
            </li>
            <li>
                <p>To recycle or properly dispose of paint or primer in your community, call 1-800-CLEANUP.</p>
            </li>
        </ul>
