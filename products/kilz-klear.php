        <h2>Surface Preparation<sup>&dagger;</sup></h2>
        <ul>
            <li>
                <p>All surfaces must be clean, free of dust, chalk,oil, grease, wax polish, mold and mildew, loose peeling paint, rust and all other foreign substances.</p>
            </li>
            <li>
                <p>If washing is necessary, use a non-soapy detergent or TSP substitute. Rinse well and allow to dry.
                </p>
            </li>
            <li>
                <p>Peeling or Checked Paint: Scrape off loose paint and sand to a smooth surface. Sanding or removal of paint containing lead is hazardous.
                </p>
            </li>
            <li>
                <p>Glossy Surfaces: Use over properly prepared glossy surfaces, scuff-sand to de-gloss and wipe clean before application.
                </p>
            </li>
            <li>
                <p>Mold &amp; Mildew Covered Surfaces: Wash the area with a mildew remover, rinse with water and allow to dry before priming.
                </p>
            </li>
        </ul>
        <h2>Application</h2>
        <ul>
            <li>
                <p>Stir thoroughly before and occasionally during use.
                </p>
            </li>
            <li>
                <p>Only apply if surface, air and product temperatures are between 50&deg; – 90&deg;F (10&deg; – 32&deg;C)</p>
            </li>
            <li>
                <p>Do not thin or apply at a greater spread rate or build up a visual glaze on the surface. Product is formulated for use at package consistency only.</p>
            </li>
            <li>
                <p>Apply using a brush, roller or sprayer.</p>
            </li>
            <li>
                <p>Prime the entire surface to ensure a uniform appearance of the topcoat.</p>
            </li>
            <li>
                <p>If rain or threatening weather is expected within 8 hours, delay applications until dry conditions exist.</p>
            </li>
            <li>
                <p>Never apply product to wet or water saturated substrates or when the relative humidity is above 90%.</p>
            </li>
            <li>
                <p>Not for use below grade or under water.</p>
            </li>
            <li>
                <p>Do not flood coat.</p>
            </li>
            <li>
                <p>Must be top coated within 7 days of application or the surface may need to be re-cleaned. To reduce waste, consult with your retailer or an online paint calculator to determine the correct amount of paint to purchase.</p>
            </li>
        </ul>
        <h2>Dry Time at 77&deg;F (25&deg;C), 50% RH:</h2>
        <ul>
            <li>
                <p>To Touch: 30 Minutes</p>
            </li>
            <li>
                <p>To Recoat: 1 Hour</p>
            </li>
            <li>
                <p>Longer dry time required in cooler temperatures and in higher humidity.</p>
            </li>
        </ul>
        <h2>Clean up & Disposal</h2>
        <ul>
            <li>
                <p>Clean equipment and paint spatters with warm, soapy water.</p>
            </li>
            <li>
                <p>Do not dispose of this product down a drain.</p>
            </li>
            <li>
                <p>If spilled, contain spilled material and remove with an inert absorbent. Dispose of contaminated absorbent, container and unused product in accordance with all current federal, state and local regulations.</p>
            </li>
            <li>
                <p>Please consider donating any unused product.</p>
            </li>
            <li>
                <p>For recycling or disposal information, contact your local household refuse collection service.</p>
            </li>
        </ul>
        <h2>Cautions / Limitations</h2>
        <ul>
            <li>
                <p>IMPORTANT! DO NOT OPEN CAN WITHOUT READING INSTRUCTIONS</p>
            </li>
            <li>
                <p>DO NOT USE ON FLOORS</p>
            </li>
            <li>
                <p>DO NOT USE WHEN AIR AND SURFACE TEMPERATURES ARE BELOW 50ºF OR ABOVE 90ºF</p>
            </li>
            <li>
                <p>SHELF LIFE UNDER NORMAL CONDITIONS IS TWO YEARS UNOPENED</p>
            </li>
        </ul>
