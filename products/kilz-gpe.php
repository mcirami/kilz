        <h2>Surface Preparation<sup>&dagger;</sup></h2>
        <ul>
            <li>
                <p>Complete all patching, repairs and sanding before applying KILZ GENERAL PURPOSE EXTERIOR.
                </p>
            </li>
            <li>
                <p>The surface must be clean, free of dust, grease, wax, mold, mildew, peeling paint and wallpaper paste. If washing is necessary use a non-soapy detergent or a TSP substitute. Rinse well and allow to dry.</p>
            </li>
            <li>
                <p>For maximum adhesion, scrape off loose paint and sand to a smooth surface. KILZ PREMIUM and KILZ 2 are recommended for use over properly prepared glossy surfaces.</p>
            </li>
            <li>
                <p>Wash the area with a mildew remover, rinse with water and allow to dry before priming. Note: KILZ PREMIUM and KILZ 2 are recommended for mildew-prone surfaces.</p>
            </li>
            <li>
                <p>KILZ General Purpose Exterior may be used on clean, dry, aged masonry surfaces. New masonry must be allowed to cure (dry) at least 30 days before applying KILZ General Purpose Exterior.</p>
            </li>
        </ul>
        <h2>Application</h2>
        <ul>
            <li>
                <p>Eye protection is recommended.
                </p>
            </li>
            <li>
                <p>A small amount of water may be added for spray applications.</p>
            </li>
            <li>
                <p>Stir or mix thoroughly before and occasionally during use.</p>
            </li>
            <li>
                <p>Apply using a brush, roller or sprayer.</p>
            </li>
            <li>
                <p>KILZ General Purpose Exterior can be topcoated with latex or oil-based paint after one hour.</p>
            </li>
            <li>
                <p>Apply only if surface, air and product temperatures are between 50&deg;-90&deg;F.</p>
            </li>
            <li>
                <p>Prime the entire surface to ensure a uniform appearance.</p>
            </li>
            <li>
                <p>May be tinted with up to 2 ounces of universal colorant per gallon. Tinting to a lighter shade than the topcoat is recommended.</p>
            </li>
            <li>
                <p>Coverage varies with porosity. Coverage is approximately 300 square feet per gallon on porous surfaces; up to 400 square feet per gallon on smooth, nonporous surfaces.</p>
            </li>
        </ul>
        <h2>Dry Time at 77&deg;F (25&deg;C), 50% RH:</h2>
        <ul>
            <li>
                <p>Dries to the touch in 30 minutes.</p>
            </li>
            <li>
                <p>Can be recoated or topcoated in one hour with latex or oil-based paint.</p>
            </li>
            <li>
                <p>Application at lower temperatures, in high humidity or in poorly ventilated areas can significantly increase dry and recoat times.</p>
            </li>
        </ul>
        <h2>Clean up &amp; Disposal</h2>
        <ul>
            <li>
                <p>Clean equipment and paint spatters with warm, soapy water.</p>
            </li>
            <li>
                <p>If spilled, contain spilled material and remove with an inert absorbent. Dispose of contaminated absorbent, container and unused product in accordance with all current federal, state and local regulations.</p>
            </li>
            <li>
                <p>To recycle or properly dispose of paint or primer in your community, call 1-800-CLEANUP.</p>
            </li>
        </ul>
