	<div class="tab_content" id="tab_1">
		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<p>The following steps are required for product performance.</p>
		<ul>
			<li>
				<p>
					The surface must be clean, free of dust, grease, wax, peeling paint, mold, mildew and wallpaper paste. If washing is necessary, use a non-soapy detergent or a TSP substitute. Rinse well and allow to dry.
				</p>
			</li>
			<li>
				<p>
					Peeling or Checked Paint: Scrape off loose paint and sand to a smooth surface. Sanding or removal of paint containing lead is hazardous.†
				</p>
			</li>
			<li>
				<p>
					Unpainted, exterior wood exposed to sun and/or moisture longer than 2-4 weeks must be cleaned and sanded before priming.
				</p>
			</li>
			<li>
				<p>
					Glossy Surfaces: For maximum adhesion, scuff sand the surface thoroughly before priming.†
				</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>
					Eye protection is recommended.
				</p>
			</li>
			<li>
				<p>
					For best results, use when substrate and air temperatures are between 35&deg;F (2&deg;C) and 90&deg;F (32&deg;C).
				</p>
			</li>
			<li>
				<p>
					Cover adjacent areas to protect from overspray.
				</p>
			</li>
			<li>
				<p>
					Prime the entire surface to ensure a uniform appearance of the topcoat.
				</p>
			</li>
			<li>
				<p>
					Shake can vigorously for 1 minute after mixing ball begins to rattle and often during use.
				</p>
			</li>
			<li>
				<p>
					Hold can upright, moving from side-to-side while spraying lightly and evenly about 12” from area being painted.
				</p>
			</li>
			<li>
				<p>
					Two to three thin coats produce best results.
				</p>
			</li>
			<li>
				<p>
					Topcoat with latex or oil-based paint after 1 hour.
				</p>
			</li>
			<li>
				<p>
					Dries to the touch in 30 minutes at 77&deg;F (25&deg;C) and 50% humidity.
				</p>
			</li>
			<li>
				<p>
					Finishing: Topcoat within 30 days to prevent contamination of the primer before painting.
				</p>
			</li>
			<li>
				<p>
					NOTE: On some surfaces, spot priming may cause a non-uniform appearance in the final coat. To avoid this, prime the entire surface.
				</p>
			</li>
		</ul>
		<h2>Dry Time at 77&deg;F (25&deg;C), 50% RH</h2>
		<ul>
			<li>
				<p>
					To Touch: 15 - 30 Minutes
				</p>
			</li>
			<li>
				<p>
					To Recoat / Topcoat: 1 Hour
				</p>
			</li>
			<li>
				<p>
					Longer dry time may be required in cooler temperatures and in higher humidity.
				</p>
			</li>
		</ul>
		<h2>Clean up & Disposal</h2>
		<ul>
			<li>
				<p>
					Do not stick pins or wire into can opening.
				</p>
			</li>
			<li>
				<p>
					Clean fresh overspray with mineral spirits or paint thinner. Remove dried paint with paint remover, but test small area first to ensure paint remover does not damage substrate.
				</p>
			</li>
			<li>
				<p>
					Please consider donating any unused product.
				</p>
			</li>
			<li>
				<p>
					For recycling or disposal information, contact your local household refuse collection service.
				</p>
			</li>
		</ul>
