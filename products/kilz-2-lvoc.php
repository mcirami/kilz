		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<ul>
			<li>
				<p>
					The surface must be clean, free of dust, grease, wax, peeling paint and wallpaper paste. If washing is necessary use a non-soapy detergent or a TSP substitute. Rinse well and allow to dry.
				</p>
			</li>
			<li>
				<p>
					For maximum adhesion, scrape off loose paint and scuff sand the surface thoroughly before priming. On glossy surfaces: scuff sand the surface thoroughly before priming.
				</p>
			</li>
			<li>
				<p>
					Unpainted, exterior wood exposed to sun and/or moisture longer than 2-4 weeks must be cleaned and sanded before priming.
				</p>
			</li>
			<li>
				<p>
					Wash the area with a mildew remover, rinse with water and allow to dry before priming.
				</p>
			</li>
			<li>
				<p>
					KILZ 2 may be used on clean, dry, aged masonry surfaces. New masonry must be allowed to cure (dry) at least 30 days before applying KILZ 2.
				</p>
			</li>
			<li>
				<p>
					It is critical to clean smoke damaged surfaces thoroughly before priming. Primary Recommendation: KILZ ORIGINAL or KILZ ODORLESS.
				</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>
					Eye protection is recommended.
				</p>
			</li>
			<li>
				<p>
					A small amount of water may be added for spray applications, but do not thin for stainblocking applications.
				</p>
			</li>
			<li>
				<p>
					Stir thoroughly before and occasionally during use.
				</p>
			</li>
			<li>
				<p>
					Apply using a brush, roller or sprayer.
				</p>
			</li>
			<li>
				<p>
					Only apply if surface, air and product temperatures are between 50&deg;-90&deg;F.
				</p>
			</li>
			<li>
				<p>
					Prime the entire surface to ensure a uniform appearance of the topcoat.
				</p>
			</li>
			<li>
				<p>
					After priming, test for stain bleed-through by applying the topcoat to a small section. If the stain bleeds through the topcoat, apply a second coat of primer and test again before topcoating the entire area. If bleeding continues, a longer dry time is needed before topcoating.
				</p>
			</li>
			<li>
				<p>
					Tinting is not recommended for stainblocking applications. KILZ 2 may be tinted with up to 2 ounces of universal colorant per gallon for non-stainblocking projects. Tinting to a lighter shade than the topcoat is recommended.
				</p>
			</li>
			<li>
				<p>
					Coverage is up to 300 square feet per gallon on porous surfaces; up to 400 square feet per gallon on smooth, nonporous surfaces.
				</p>
			</li>
		</ul>
		<h2>Dry Time at 77&deg;F (25&deg;C), 50% RH</h2>
		<ul>
			<li>
				<p>
					Dries to the touch in 30 minutes.
				</p>
			</li>
			<li>
				<p>
					Can be recoated or topcoated in one hour with latex or oil-based paint.
				</p>
			</li>
			<li>
				<p>
					Application at lower temperatures, in high humidity or in poorly ventilated areas will affect dry time.
				</p>
			</li>
		</ul>
		<h2>Clean up & Disposal</h2>
		<ul>
			<li>
				<p>
					Clean equipment and paint spatters with warm, soapy water.
				</p>
			</li>
			<li>
				<p>
					Dispose of contaminated absorbent, container and unused product in accordance with all current federal, state and local regulations.
				</p>
			</li>
			<li>
				<p>
					To recycle or properly dispose of paint or primer in your community, call 1-800-CLEANUP.
				</p>
			</li>
		</ul>
