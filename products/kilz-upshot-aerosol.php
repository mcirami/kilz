	<div class="tab_content" id="tab_1">
		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<ul>
			<li>
				<p>
					The surface must be clean, free of dust, grease, wax, peeling paint, mold, mildew, wallpaper paste, etc. If washing is necessary, use a non-soapy detergent or a TSP substitute. Rinse well and allow to dry.
				</p>
			</li>
			<li>
				<p>
					Peeling or Checked Paint: Scrape off loose paint and sand to a smooth surface. Sanding or removal of paint containing lead is hazardous.
				</p>
			</li>
			<li>
				<p>
					Glossy Surfaces: For maximum adhesion, scuff sand the surface thoroughly before priming.
				</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>
					Eye protection is recommended.
				</p>
			</li>
			<li>
				<p>
					For best results, use at room temperature. Cover adjacent areas to protect from overspray.
				</p>
			</li>
			<li>
				<p>
					Shake can vigorously for 1 minute after mixing ball begins to rattle and often during use. Hold can upright, moving from side-to-side while spraying lightly and evenly, about 12" from area being painted. Two to three thin coats produce best results.
				</p>
			</li>
			<li>
				<p>
					Topcoat with latex or oil-based paint after 15 minutes. Note: On some surfaces, spot priming may cause a non-uniform appearance in the final coat. To avoid this, prime the entire surface.
				</p>
			</li>
		</ul>
		<h2>Dry Time at 77°F (25°C), 50% RH</h2>
		<ul>
			<li>
				<p>
					Topcoat with latex or oil-based paint after 15 minutes.
				</p>
			</li>
		</ul>
		<h2>Clean up & Disposal</h2>
		<ul>
			<li>
				<p>
					Clear spray valve immediately after use by turning can upside down and spraying until only clear gas is released. If spray nozzle becomes clogged, remove and clean. Do not stick pins or wire into can opening.
				</p>
			</li>
			<li>
				<p>
					Replace nozzle with valve opening pointed away from you. Clean fresh overspray with mineral spirits or paint thinner. Remove dried paint with paint remover, but test small area first to ensure paint remover does not damage substrate.
				</p>
			</li>
			<li>
				<p>
					To recycle or properly dispose of paint or primer in your community, call 1-800-CLEANUP.
				</p>
			</li>
		</ul>
