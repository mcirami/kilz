		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<ul>
			<li>
				<p>
					All surfaces must be clean, sound, dry and free of any dirt, oil, and grease before application.
				</p>
			</li>
			<li>
				<p>
					Wood Surfaces: Replace rotting or unstable boards and remove loose splinters.
				</p>
			</li>
			<li>
				<p>
					Scuff sand glossy surfaces.
				</p>
			</li>
			<li>
				<p>
					Remove mildew, stains, dirt, oil and grease with a product such as KILZ Wood Cleaner & Brightener prior to application.
				</p>
			</li>
			<li>
				<p>
					Use a product such as KILZ Wood Stain & Finish Stripper (must be followed by KILZ Wood Cleaner & Brightener) to remove all loose wood fibers, peeling paint/stain or deteriorated coatings prior to application of KILZ OVER ARMOR.
				</p>
			</li>
			<li>
				<p>
					On new wood, use KILZ Wood Cleaner & Brightener to remove mill glaze and open the pores of the wood. Rinse thoroughly and allow to dry before coating.
				</p>
			</li>
			<li>
				<p>
					Concrete Surfaces: Avoid areas that are structurally weak or subject to hydrostatic pressure.
				</p>
			</li>
			<li>
				<p>
					Allow new surfaces to cure for 30 days.
				</p>
			</li>
			<li>
				<p>
					Use a product such as KILZ Concrete & Masonry Paint Stripper to strip peeling coating.
				</p>
			</li>
			<li>
				<p>
					Use a product such as KILZ Concrete & Masonry Cleaner & Etcher to clean and etch uncoated concrete surfaces prior to coating.
				</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>
					<strong>MUST PREP BEFORE USE.</strong>
				</p>
			</li>
			<li>
				<p>
					Wood surface must be completely dry prior to painting.
				</p>
			</li>
			<li>
				<p>
					Intermix multiple containers to ensure color uniformity.
				</p>
			</li>
			<li>
				<p>
					Avoid exposure of container to direct sunlight.
				</p>
			</li>
			<li>
				<p>
					Temperature Range: 50-90° F.
				</p>
			</li>
		</ul>
		<h2>Application Methods</h2>
		<ul>
			<li>
				<p>
					Nylon/polyester brush (for edges, corners and in between boards).
				</p>
			</li>
			<li>
				<p>
					1/4" - 1/2" nap roller. For smoother textures, use shorter nap.
				</p>
			</li>
			<li>
				<p>
					For nail holes and cracks up to 1/4, completely fill in with KILZ OVER ARMOR coating using a nylon/polyester brush and allow to dry prior to rolling. Deep nail holes and cracks may require additional filling.
				</p>
			</li>
			<li>
				<p>
					Use pressure while rolling and apply generously.
				</p>
			</li>
		</ul>
		<h2>Dry Time*</h2>
		<ul>
			<li>
				<p>
					Recoat after 4-6 hours Wait 24-48 hours before light foot traffic. 72 hours for full cure. *Cool temperatures and/or high humidity will require additional drying time.
				</p>
			</li>
			<li>
				<p>
					2 coats required.
				</p>
			</li>
			<li>
				<p>
					For optimum performance, coat all six sides of wood.
				</p>
			</li>
		</ul>
		<h2>Clean-Up & Disposal</h2>
		<ul>
			<li>
				<p>
					Brushes and other equipment can be cleaned with warm, soapy water.
				</p>
			</li>
			<li>
				<p>
					For disposal of empty containers and unused product, contact your household refuse collection service.
				</p>
			</li>
		</ul>
