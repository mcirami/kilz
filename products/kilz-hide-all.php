		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<ul>
			<li>
				<p>All surfaces must be clean, free of dust, chalk,oil, grease, wax polish, mold and mildew, loose peeling paint, rust and all other foreign substances.</p>
			</li>
			<li>
				<p>If washing is necessary, use a non-soapy detergent or TSP substitute. Rinse well and allow to dry.</p>
			</li>
			<li>
				<p>Peeling or Checked Paint: Scrape off loose paint and sand to a smooth surface. Sanding or removal of paint containing lead is hazardous.</p>
			</li>
			<li>
				<p>Glossy Surfaces - Not Recommended: Use products such as KILZ® ADHESION, KILZ MAX® and KILZ® PREMIUM primers over properly prepared glossy surfaces.</p>
			</li>
			<li>
				<p>Always wash mold or mildew covered surfaces with a mildew remover, rinse with water and allow to dry before priming.</p>
			</li>
			<li>
				<p>Mold or Mildew Prone Surfaces - Not Recommended: use products such as KILZ PREMIUM and KILZ 2® primers over properly prepared, mold- or mildew-prone areas.</p>
			</li>
			<li>
				<p>Masonry, Brick, Stucco and Plaster: KILZ HIDE-ALL may be used on clean, dry, aged interior masonry surfaces that have cured (dried) at least 30 days.</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>Eye protection is recommended.</p>
			</li>
			<li>
				<p>Only apply if surface, air and product temperatures are between 50&deg; – 90&deg;F (10&deg; – 32&deg;C).</p>
			</li>
			<li>
				<p>Apply using a brush, roller or sprayer.</p>
			</li>
			<li>
				<p>Only apply if surface, air and product temperatures are between 50&deg;-90&deg;F.</p>
			</li>
			<li>
				<p>Brush – High quality Nylon/Polyester.</p>
			</li>
			<li>
				<p>Roller – High quality 3/8-1/2" nap on smooth surfaces or 1/2-3/4" nap on semi-rough or porous surfaces.</p>
			</li>
			<li>
				<p>Airless spray – .017 - .021 tip / 60 mesh filter @ 1,800 – 2,500 PSI.</p>
			</li>
			<li>
				<p>DO NOT THIN. Product is formulated for use at package consistency. For spray applications, a small amount of water (no more than ½ pint per gallon) may be added.</p>
			</li>
			<li>
				<p>Stir thoroughly before and occasionally during use.</p>
			</li>
			<li>
				<p>Stains: KILZ HIDE-ALL is formulated to hide minor, non-bleeding scuffs and stains before topcoating; for heavier stains use a product such as KILZ MAX, KILZ PREMIUM, KILZ® ORIGINAL, KILZ COMPLETE® or KILZ ODORLESS® primers.</p>
			</li>
			<li>
				<p>Tinting: May be tinted with up to 2 oz. (60 mL) of universal colorant per gallon. Tinting to a lighter shade than the topcoat is recommended.</p>
			</li>
			<li>
				<p>Coverage: 300 - 400 sq. ft.(28 - 37 m2) per gallon depending on application method and surface porosity.</p>
			</li>
		</ul>
		<h2>Dry Time at 77&deg;F (25&deg;C), 50% RH:</h2>
		<ul>
			<li>
				<p>To Touch: 30 Minutes</p>
			</li>
			<li>
				<p>Can be recoated or topcoated in one hour with latex or oil-based paint.</p>
			</li>
			<li>
				<p>Application at lower temperatures, in high humidity or in poorly ventilated areas will affect dry time.</p>
			</li>
		</ul>
		<h2>Clean up & Disposal</h2>
		<ul>
			<li>
				<p>Clean equipment and paint spatters with warm, soapy water.</p>
			</li>
			<li>
				<p>If spilled, contain material and remove with an inert absorbent. Dispose of contaminated absorbent, container and unused product in accordance with all current federal, state and local regulations.</p>
			</li>
			<li>
				<p>For recycling or disposal information contact your local household refuse collection service.</p>
			</li>
		</ul>
		<h2>Cautions / Limitations</h2>
		<ul>
			<li>
				<p>IMPORTANT! DO NOT OPEN CAN WITHOUT READING INSTRUCTIONS</p>
			</li>
			<li>
				<p>INTERIOR USE ONLY</p>
			</li>
			<li>
				<p>DO NOT USE ON FLOORS</p>
			</li>
			<li>
				<p>DO NOT USE WHEN AIR AND SURFACE TEMPERATURES ARE BELOW 50&deg;F OR ABOVE 90&deg;F</p>
			</li>
			<li>
				<p>DO NOT USE OVER GLOSY SURFACES – KILZ PREMIUM AND KILZ 2 ARE RECOMMENDED FOR USE OVER PROPERLY PREPARED GLOSSY SURFACES</p>
