        <h2>Surface Preparation<sup>&dagger;</sup></h2>
        <ul>
            <li>
                <p>Remove any mildew stains with a bleach and water solution or similar commercially available mildew remover and rinse.
                </p>
            </li>
            <li>
                <p>For maximum adhesion, scrape off loose paint and scuff sand the surface thoroughly before priming. On glossy surfaces: scuff sand the surface thoroughly before priming.
                </p>
            </li>
            <li>
                <p>Remove loose paint and wash off any dirt or grease with a non-soapy detergent. Rinse well and then allow to dry.
                </p>
            </li>
            <li>
                <p>Make sure that there are no active water problems and that any previous water damage is fully dry. Either KILZ ORIGINAL or KILZ COMPLETE is recommended to block severe water stains.
                </p>
            </li>
            <li>
                <p>Standard drywall preparation techniques including sanding should still be exercised to ensure success.
                </p>
            </li>
        </ul>
        <h2>Application</h2>
        <ul>
            <li>
                <p>Use only when temperatures are between 50&deg;F (10&deg;C) and 90&deg;F (32&deg;C).
                </p>
            </li>
            <li>
                <p>Stir completely prior to use.</p>
            </li>
            <li>
                <p>For the best performance, use an airless sprayer with fluid pressure of at least 2,000 psi. Tips should be between .017 and .021. For best results, apply a film 12 to 16 mils wet; this will provide coverage between 100 to 150 sq. ft. per gallon. Backrolling is not required and will cause surface defects.</p>
            </li>
            <li>
                <p>If using manual applicators, choose only a high-quality roller or brush. When applied at 6 to 8 mils wet, this method will provide coverage between 200 to 250 square foot per gallon.</p>
            </li>
            <li>
                <p>Coverage can vary significantly based on surface porosity.</p>
            </li>
        </ul>
        <h2>Dry Time at 77&deg;F (25&deg;C), 50% RH:</h2>
        <ul>
            <li>
                <p>Dries to touch in 1 hour at 77&deg;F (25&deg;C) and 50% humidity.</p>
            </li>
        </ul>
        <h2>Clean up & Disposal</h2>
        <ul>
            <li>
                <p>Use soap and water to clean hands, tools and spills.</p>
            </li>
            <li>
                <p>For recycling or disposal information in your community, call 1-800-CLEANUP.</p>
            </li>
            <li>
                <p>CAUTION! Avoid contact with eyes and skin. Avoid breathing of dust, vapors and spray mist.</p>
            </li>
        </ul>
