	<div class="tab_content" id="tab_1">
		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<ul>
			<li>
				<p>The surface must be clean, free of dust, grease, wax, peeling paint, mold, mildew and wallpaper paste. If washing is necessary, use a non-soapy detergent or a TSP substitute. Rinse well and allow to dry.</p>
			</li>
			<li>
				<p>Peeling or Checked Paint: Scrape off loose paint and sand to a smooth surface. Sanding or removal of paint containing lead is hazardous.<sup>†</sup></p>
			</li>
			<li>
				<p>Mold or Mildew Covered Surfaces: Wash the area with a mildew remover, rinse with water and allow to dry before priming.</p>
			</li>
			<li>
				<p>Masonry, Brick, Stucco and Plaster: KILZ MAX primer may be used on clean, dry, aged masonry surfaces. New masonry must be allowed to cure (dry) at least 30 days before applying KILZ MAX primer.</p>
			</li>
			<li>
				<p>Fire Restoration: It is critical to clean smoke damaged surfaces thoroughly before priming.</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>Eye protection is recommended.</p>
			</li>
			<li>
				<p>Apply as supplied using a brush, roller or sprayer.</p>
			</li>
			<li>
				<p>Do not thin for stainblocking applications. A small amount of water may be added for other spray applications.</p>
			</li>
			<li>
				<p>Only apply if surface, air and product temperatures are between 50&deg;-90&deg;F (10&deg;- 32&deg;C).</p>
			</li>
			<li>
				<p>Stir thoroughly before and occasionally during use.</p>
			</li>
			<li>
				<p>Prime the entire surface to ensure a uniform appearance of the topcoat.</p>
			</li>
			<li>
				<p>Stainblocking: After priming, test for stain bleed-through by applying the topcoat to a small section. If the stain bleeds through the topcoat, apply a second coat of primer and test again before topcoating the entire area. If bleeding continues, a longer dry time is needed before topcoating.</p>
			</li>
			<li>
				<p>Sealing odors: For persistent odors, apply a second coat of primer before topcoating. If odor continues, a longer dry time is needed before topcoating.</p>
			</li>
			<li>
				<p>Tinting: Not recommended for stainblocking applications. KILZ MAX primer may be tinted with up to 2 ounces of universal colorant per gallon for non- stainblocking projects. Tinting to a lighter shade than the topcoat is recommended.</p>
			</li>
		</ul>
		<h2>Dry Time at 77&deg;F (25&deg;C), 50% RH</h2>
		<ul>
			<li>
				<p>To Touch: 30 Minutes</p>
			</li>
			<li>
				<p>To Recoat / Topcoat: 1 Hour</p>
			</li>
			<li>
				<p>Application at lower temperatures, high humidity or poor ventilation will affect dry time.</p>
			</li>
		</ul>
		<h2>Clean up & Disposal</h2>
		<ul>
			<li>
				<p>Clean equipment and paint spatters with warm, soapy water.</p>
			</li>
			<li>
				<p>If spilled, contain material and remove with an inert absorbent. Dispose of contaminated absorbent, container and unused product in accordance with all current federal, state and local regulations.</p>
			</li>
			<li>
				<p>Do not dispose of this product down a drain.</p>
			</li>
			<li>
				<p>Please consider donating any unused product.</p>
			</li>
			<li>
				<p>For recycling or disposal information, contact your local household refuse collection service.</p>
			</li>
		</ul>
