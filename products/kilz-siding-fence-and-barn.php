	<div class="tab_content" id="tab_1">
		<h2>Surface Preparation<sup>&dagger;</sup></h2>
		<ul>
			<li>
				<p>Remove any mildew stains with a bleach and water solution or similar commercially available mildew remover then rinse.</p>
			</li>
			<li>
				<p>Sand all glossy surfaces.</p>
			</li>
			<li>
				<p>To determine the condition of the surface, place cloth duct tape in several places on the bare wood. Press the tape down firmly then remove quickly. If wood fibers or debris are pulled onto the tape, additional preparation is required as follows:</p>
			</li>
			<li>
				<p>If debris is found using the above test, remove any loose or peeling paint† and wash off dirt or grease with a non-soapy detergent such as TSP substitute. Remove mill glaze, loose wood fibers, dirt and other debris. Rinse with water and allow to dry completely.</p>
			</li>
			<li>
				<p>If surface is in good condition, apply directly over clean, dry surface.</p>
			</li>
		</ul>
		<h2>Application</h2>
		<ul>
			<li>
				<p>Do not use if temperature is below 50°F (10°C) or if rain is expected within 24 hours.</p>
			</li>
			<li>
				<p>All surfaces must be completely dry.</p>
			</li>
			<li>
				<p>Intermix containers of the same color to ensure color uniformity.</p>
			</li>
			<li>
				<p>Many woods contain tannin which may cause discoloration. Prime these woods with one coat of KILZ PREMIUM to reduce these stains as needed.</p>
			</li>
			<li>
				<p>Coverage varies by porosity. Coverage will be approximately 200 square feet per gallon on porous surfaces; up to 500 square feet per gallon on smooth, nonporous surfaces.</p>
			</li>
			<li>
				<p>Keep a wet edge and always back-brush or back-roll during all application methods.</p>
			</li>
			<li>
				<p>If two coats are necessary, apply the second coat only after the first coat has thoroughly dried.</p>
			</li>
		</ul>
		<h2>Dry Time at 77°F (25°C), 50% RH</h2>
		<ul>
			<li>
				<p>Dries to the touch in 2-3 hours.</p>
			</li>
			<li>
				<p>Allow 6 to 8 hours of drying prior to recoating.</p>
			</li>
		</ul>
		<h2>Clean-Up & Disposal</h2>
		<ul>
			<li>
				<p>Use soap and water to clean hands, tools and spills.</p>
			</li>
			<li>
				<p> For recycling or disposal information in your community, call 1-800-CLEANUP.</p>
			</li>
			<li>
				<p><strong>CAUTION: USE ONLY WITH ADEQUATE VENTILATION.</strong></p>
			</li>
		</ul>
