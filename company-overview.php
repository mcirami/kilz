		<div class="column">
			<p>Melvin Caldwell, the founder of what would one day become Masterchem Industries LLC, started business in 1954 as a Mautz Paint dealer on Easton Avenue in St. Louis, Mo. Along with the Mautz paint line, he carried products from a local paint manufacturing company known as P.D. George.</p>
			<p>Soon after, Mr. Caldwell rented a retail space on Woodson Road in St. Louis County and started manufacturing the Caldwell Paint line in a used 50-gallon dough mixer that he bought from a bakery. His company at the time was known as Color Wheel.</p>
			<p>In 1961, Mr. Caldwell moved his manufacturing process to a building on Trenton Avenue in North St. Louis County and employed two people to mix 400-gallon batches of paint. Over the years, his company continued to grow and required larger and larger facilities, eventually moving to Kirkwood, Mo. in a 14,000 square foot facility.</p>
			<p>Mr. Caldwell introduced the KILZ® brand in 1974 at the National Paint Show. His company's "Masterchem" name was created to promote the flagship product, KILZ primer, nationally. Soon afterward, other specialty primers were added to the product line. Masterchem was a subsidiary of Caldwell Paint Manufacturing Company, Inc., until 1990.</p>
			<p>In 1978, the Masterchem plant was moved to a new facility on Utah Avenue in South St. Louis City. At this location, the staff consisted of 25 office, production and janitorial workers.</p>
		</div>
		<div class="column">
			<p>The company owned its two local retail outlets, and Caldwell Paint was well known in the St. Louis area. The paint line was also sold in Peoria, Ill. through a retail store owned and managed by an independent dealer.</p>
			<p>When a 64,000 square foot building became available in the same general area, the plant was moved in 1982. This building (on Fyler Avenue) was formerly a soft drink bottling plant and took four months to convert for paint manufacturing.</p>
			<p>In 1982, Melvin Caldwell retired as CEO and Robert Caldwell became President. Under Robert Caldwell’s supervision, the Imperial plant was purchased in December of 1988. The 157,000 square foot building sits on 11.2 acres. After nearly a year of renovation, the Masterchem team moved from Fyler to Imperial in July 1989.</p>
			<p>Caldwell Paint remained our local trade sale outlet until 1990-91 when the last remaining retail store was closed. The company, which was a family business, was sold in 1986 to Reed Holdings, PLC and then again to Williams Holdings, PLC in 1987.</p>
			<p>Masco Corporation bought Masterchem in 2000.</p>
